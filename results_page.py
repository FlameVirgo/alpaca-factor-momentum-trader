#!/usr/bin/env python3
"""
Results page — reads the Alpaca paper account and writes `results.md`: equity,
P&L since inception, max drawdown, positions, recent fills, and how the account
is doing vs buy-and-hold SPY over the same period.

Run by a GitHub Action every Mon/Wed/Fri after the close, which commits the
refreshed page. Those commits double as repo activity, so GitHub never hits the
60-day inactivity pause on the scheduled workflows. Read-only: places no orders.
"""
from __future__ import annotations

from datetime import datetime, timedelta, timezone
from pathlib import Path

from config import AlpacaCredentials, BENCHMARK
from data.alpaca_data import trading_client, get_daily_closes

OUT_PATH = Path(__file__).resolve().parent / "results.md"
FILLS_WINDOW_DAYS = 30
FILLS_MAX_ROWS = 25
EQUITY_TAIL_ROWS = 15


def _equity_history(tc):
    """[(date, equity), ...] since inception via Alpaca portfolio history."""
    try:
        from alpaca.trading.requests import GetPortfolioHistoryRequest
        ph = tc.get_portfolio_history(
            GetPortfolioHistoryRequest(period="1A", timeframe="1D"))
        return [(datetime.fromtimestamp(t, tz=timezone.utc).date(), float(e))
                for t, e in zip(ph.timestamp or [], ph.equity or []) if e]
    except Exception:
        return []


def _max_drawdown(equities) -> float:
    peak, mdd = float("-inf"), 0.0
    for e in equities:
        peak = max(peak, e)
        mdd = min(mdd, e / peak - 1.0)
    return mdd


def _recent_fills(tc, after: datetime):
    from alpaca.trading.requests import GetOrdersRequest
    from alpaca.trading.enums import QueryOrderStatus
    try:
        orders = tc.get_orders(GetOrdersRequest(status=QueryOrderStatus.CLOSED,
                                                after=after, limit=500))
        fills = [o for o in orders if str(o.status).endswith("FILLED")]
        return sorted(fills, key=lambda o: o.filled_at or after, reverse=True)
    except Exception:
        return []


def _spy_return_since(creds, start_date) -> "float | None":
    """Buy-and-hold SPY total return from `start_date` to the latest close."""
    try:
        start = datetime(start_date.year, start_date.month, start_date.day,
                         tzinfo=timezone.utc)
        px = get_daily_closes([BENCHMARK], start=start - timedelta(days=7), creds=creds)
        s = px[BENCHMARK].dropna()
        since = s[s.index >= start.replace(tzinfo=None)]
        ref = since.iloc[0] if len(since) else s.iloc[0]
        return float(s.iloc[-1] / ref - 1.0)
    except Exception:
        return None


def _fmt_pct(x):
    return "n/a" if x is None else f"{x:+.2%}"


def _fmt_usd(x):
    return "n/a" if x is None else f"${x:,.2f}"


def build_page() -> str:
    creds = AlpacaCredentials.from_env()
    tc = trading_client(creds)
    now = datetime.now(timezone.utc)

    acct = tc.get_account()
    equity = float(acct.equity)
    cash = float(acct.cash)
    # Post-June-2026 intraday margin framework: `buying_power` is the one
    # sanctioned field (the daytrading_* / PDT fields were removed from the API).
    buying_power = float(acct.buying_power)

    history = _equity_history(tc)
    positions = tc.get_all_positions()
    fills = _recent_fills(tc, now - timedelta(days=FILLS_WINDOW_DAYS))

    start_date, start_eq = (history[0] if history else (None, None))
    total_pl = equity - start_eq if start_eq else None
    total_pl_pct = (equity / start_eq - 1.0) if start_eq else None
    mdd = _max_drawdown([e for _, e in history]) if history else None
    spy_ret = _spy_return_since(creds, start_date) if start_date else None

    lines = [
        "# Live Paper-Trading Results",
        "",
        f"Core-satellite strategy on the Alpaca **paper** account "
        f"(simulated fills, real market data). Auto-generated by "
        f"`results_page.py` — do not edit by hand.",
        "",
        f"_Last updated: {now:%Y-%m-%d %H:%M} UTC_",
        "",
        "## Account",
        "",
        "| | |",
        "|---|---:|",
        f"| Equity | **{_fmt_usd(equity)}** |",
        f"| Cash | {_fmt_usd(cash)} |",
        f"| Buying power | {_fmt_usd(buying_power)} |",
        f"| Open positions | {len(positions)} |",
        "",
        "## Performance since inception" + (f" ({start_date:%Y-%m-%d})" if start_date else ""),
        "",
        "| | |",
        "|---|---:|",
        f"| Starting equity | {_fmt_usd(start_eq)} |",
        f"| Total P&L | **{_fmt_usd(total_pl)} ({_fmt_pct(total_pl_pct)})** |",
        f"| Max drawdown | {_fmt_pct(mdd)} |",
        f"| Buy-and-hold SPY, same period | {_fmt_pct(spy_ret)} |",
    ]
    if total_pl_pct is not None and spy_ret is not None:
        lines.append(f"| **Vs SPY** | **{_fmt_pct(total_pl_pct - spy_ret)}** |")

    lines += ["", f"## Positions ({len(positions)})", ""]
    if positions:
        lines += ["| Symbol | Qty | Market value | Unrealized P&L |",
                  "|---|---:|---:|---:|"]
        for p in sorted(positions, key=lambda x: -float(x.market_value)):
            lines.append(
                f"| {p.symbol} | {float(p.qty):,.4f} | {_fmt_usd(float(p.market_value))} "
                f"| {_fmt_usd(float(p.unrealized_pl))} ({float(p.unrealized_plpc):+.2%}) |")
    else:
        lines.append("_No open positions._")

    lines += ["", f"## Fills, last {FILLS_WINDOW_DAYS} days ({len(fills)})", ""]
    if fills:
        lines += ["| Symbol | Side | Fill price | Qty | When (UTC) |",
                  "|---|---|---:|---:|---|"]
        for o in fills[:FILLS_MAX_ROWS]:
            px = _fmt_usd(float(o.filled_avg_price)) if o.filled_avg_price else "—"
            lines.append(f"| {o.symbol} | {o.side.value.upper()} | {px} "
                         f"| {float(o.filled_qty):,.4f} | {o.filled_at:%Y-%m-%d %H:%M} |")
        if len(fills) > FILLS_MAX_ROWS:
            lines.append(f"| … | | | | +{len(fills) - FILLS_MAX_ROWS} more |")
    else:
        lines.append("_No fills in the window._")

    lines += ["", f"## Daily equity, last {EQUITY_TAIL_ROWS} sessions", ""]
    if history:
        lines += ["| Date | Equity | Day change |", "|---|---:|---:|"]
        tail = history[-EQUITY_TAIL_ROWS:]
        prev = history[-EQUITY_TAIL_ROWS - 1][1] if len(history) > EQUITY_TAIL_ROWS else None
        for d, e in tail:
            chg = _fmt_pct(e / prev - 1.0) if prev else "n/a"
            lines.append(f"| {d:%Y-%m-%d} | {_fmt_usd(e)} | {chg} |")
            prev = e
    else:
        lines.append("_Portfolio history unavailable._")

    lines += ["", "---", "",
              "Paper trading (simulated). Strategy spec and backtest in "
              "[README.md](README.md) / [PLAN.md](PLAN.md).", ""]
    return "\n".join(lines)


def main() -> None:
    OUT_PATH.write_text(build_page())
    print(f"Wrote {OUT_PATH}")


if __name__ == "__main__":
    main()
